public class DeclareVariables{
	public static void main(String[] args){
		System.out.println("Declaring Variables");
		// Declare a variable of type int with a name of i
		int i;
		// Set the variable of i to 100
		i = 100;
		// You can do both of those on a single line
		// Declare an int named num to 50
		int num = 50;
		// Display the value
		System.out.println("The value of i is " + i);
		System.out.println("The value of num is " + num);
		// Declare a variable of type String with a name of s and set the value
		String s = "My first string variable";
		// Display the value
		System.out.println("The value of s is " + s);
	}
}